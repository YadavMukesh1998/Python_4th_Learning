#Operators = a symbol which will perform an operation between 2 or more variables.

# 1. Arithmetic Operators (+,_,*)
# | Operator | Meaning             | Example (`10`, `3`) | Result     |
# | -------- | ------------------- | ------------------- | ---------- |
# | `/`      | Division (float)    | `10 / 3`            | `3.333...` |
# | `//`     | Floor Division      | `10 // 3`           | `3`        |
# | `%`      | Modulus (remainder) | `10 % 3`            | `1`        |

# 2. Logical Operators (compare boolean)
# 3. Relational Operators (compare some values)


# Arithmetic Operators (+,_,*,/,%,**)
a = 10
b = 3
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)  #a // b → Quotient (how many times b fits into a without considering the remainder)
print(a%b)   #a % b → Remainder (what's left after taking out b * quotient from a)
print(a**b)  #a^b

